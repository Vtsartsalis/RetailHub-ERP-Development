package finalVersion;

public enum OrderStatus {
    PENDING,             // Εκκρεμεί (αρχική κατάσταση αν δεν υπάρχει καθόλου απόθεμα)
    PARTIALLY_FULFILLED, // Μερικώς εκπληρωμένη (κάποια προϊόντα δεσμεύτηκαν αλλά όχι όλα)
    READY_TO_BE_DELIVERED, // Όλα τα προϊόντα είναι πλέον διαθέσιμα για παράδοση
    FULFILLED,           // Τελική Κατάσταση: Η παραγγελία παραδόθηκε και η πώληση καταγράφηκε
    CANCELED             // Ακυρωμένη
}